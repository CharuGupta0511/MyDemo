myFruitList = ["apple", "banana", "cherry"]   #Create a List
print(myFruitList)          #Print List
print(type(myFruitList))    #Print type of List

print(myFruitList[0])       #Print first element in the list
print(myFruitList[1])       #Print second element in the list
print(myFruitList[2])       #Print third element in the list

myFruitList[2] = "orange"   #Replace cherry with orange
print(myFruitList)


